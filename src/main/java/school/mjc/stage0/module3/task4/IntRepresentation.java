package school.mjc.stage0.module3.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        int c = (int) 'c';
        int h = (int) 'h';
        int a = (int) 'a';
        int r = (int) 'r';

        System.out.println(c); // выводит 99
        System.out.println(h); // выводит 104
        System.out.println(a); // выводит 97
        System.out.println(r); // выводит 114
    }
}
